     public void crear() {
        ProductosDAO cl = new ProductosDAO();
         try {
             cl.crear(Integer.parseInt(txt_codigo.getText()), txt_nombre.getText(),Integer.parseInt(txt_cantidad.getText()), txt_lugar.getText(), txt_fecha.getText());
         } catch (NumberFormatException e) {
             JOptionPane.showMessageDialog(null, "   NO SE PERMITEN CAMPOS VACIOS");          
         }
    }
     
