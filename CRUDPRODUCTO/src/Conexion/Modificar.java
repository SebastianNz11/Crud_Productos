    public void modificar() {

        ProductosDAO pd = new ProductosDAO();
        try {
            pd.modificar(Integer.parseInt(txt_codigo.getText()), txt_nombre.getText(), Integer.parseInt(txt_cantidad.getText()), txt_lugar.getText(), txt_fecha.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "   NO SE PERMITEN CAMPOS VACIOS");
        }
        Menu mp = new Menu();
        mp.setVisible(true);
        mp.setLocationRelativeTo(null);
        dispose();

    }

    public void mostrar(int codigo, String nombre, int cantidad, String lugar_fabricacion, String fecha_ingreso) {
        txt_codigo.setText(codigo + "");
        txt_nombre.setText(nombre);
        txt_cantidad.setText(cantidad + "");
        txt_lugar.setText(lugar_fabricacion);
        txt_fecha.setText(fecha_ingreso);
    }

